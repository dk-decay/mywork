    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:

        int selectedRow = newDrugTable.getSelectedRow();
        if (selectedRow >= 0) {
            Drug drug = (Drug) newDrugTable.getValueAt(selectedRow, 0);
            EditNewDrugPanel panel = new EditNewDrugPanel(container, system, enterprise, organization, user, drug);
            CardLayout layout = (CardLayout) container.getLayout();
            container.add("EditNewDrugPanel", panel);
            layout.next(container);
        } else {
            JOptionPane.showMessageDialog(null, "Please Select a compound request to be modified");
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = newDrugTable.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete ?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Drug drug = (Drug) newDrugTable.getValueAt(selectedRow, 0);

                this.system.getDrugDirectory().removeDrug(drug);
                this.enterprise.getDrugList().remove(drug);
                this.organization.getDrugList().remove(drug);
                this.populateNewDrugTable();
                updateWorkOrderRequest(drug);
                JOptionPane.showMessageDialog(null, "New Compound Request deleted succesfully");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please Select a compound request to be deleted");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    public void updateWorkOrderRequest(Drug drug) {
        WorkRequest workRequest = retrieveWorkRequest(drug, WorkRequestStatus.NEW, WorkRequestStatus.UPDATED);
        workRequest.setStatus(WorkRequestStatus.CANCELLED);

    }

    public WorkRequest retrieveWorkRequest(Drug drug, WorkRequestStatus s1, WorkRequestStatus s2) {

        WorkRequest request = this.enterprise.getWorkQueue().getWorkRequestList().stream()
                .filter(wr -> wr.getAttachment().getAttachmentType() == AttachmentType.NewDrugApprovalAttachment)
                .filter(fwr -> {
                    NewDrugApprovalAttachment attach
                            = (NewDrugApprovalAttachment) fwr.getAttachment();
                    if (attach.getDrug() == drug && (fwr.getStatus()
                            == s1 || fwr.getStatus() == s2)) {
                        return true;
                    } else {
                        return false;
                    }
                })
                .findFirst()
                .get();
        return request;
    }


