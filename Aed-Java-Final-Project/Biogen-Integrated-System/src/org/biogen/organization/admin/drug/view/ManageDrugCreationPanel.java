    public void populateNewDrugTable() {

        DefaultTableModel model = (DefaultTableModel) newDrugTable.getModel();

        model.setRowCount(0);
        System.out.println("drugs size : " + this.organization.getDrugList().size());
        this.organization.getDrugList().stream()
                .filter(d -> d.getStatus() != Status.APPROVED && d.getStatus() != Status.REJECTED)
                .forEach(dr -> {
                    Object[] row = new Object[4];
                    row[0] = dr;
                    row[1] = dr.getDisease();
                    row[2] = dr.getCreatedOn();
                    row[3] = dr.getStatus().toString();
                    model.addRow(row);
                });

    }

    public void populateApprovedOrDeniedCompoundRequest() {
        DefaultTableModel model = (DefaultTableModel) approvedDrugTable.getModel();

        model.setRowCount(0);
        this.organization.getDrugList().stream()
                .filter(com -> com.getStatus() == Status.APPROVED
                || com.getStatus() == Status.REJECTED)
                .forEach(dr -> {
                    Object[] row = new Object[5];
                    row[0] = dr;
                    row[1] = dr.getDisease();
                    row[2] = dr.getCreatedOn();
                    row[3] = dr.getStatus().toString();
                    if (dr.getStatus() == Status.REJECTED) {
                        WorkRequest request = retrieveWorkRequest(dr, WorkRequestStatus.APPROVED, WorkRequestStatus.DENIED);
                        row[4] = request.getComments();
                    } else {
                        row[4] = "";
                    }
                    model.addRow(row);
                });

    }

