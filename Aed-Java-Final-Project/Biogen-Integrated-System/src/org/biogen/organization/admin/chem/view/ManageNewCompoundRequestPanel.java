    public void populateNewCompoundRequest() {
        DefaultTableModel model = (DefaultTableModel) newCompoundTable.getModel();

        model.setRowCount(0);
        this.organization.getCompoundList().stream()
                .filter(com -> com.getStatus() != Status.APPROVED && com.getStatus() != Status.REJECTED)
                .forEach(c -> {
                    Object[] row = new Object[4];
                    row[0] = c;
                    row[1] = c.getFormula();
                    row[2] = c.getCreatedOn();
                    row[3] = c.getStatus().toString();
                    model.addRow(row);
                });

    }

    public void populateApprovedOrDeniedCompoundRequest() {
        DefaultTableModel model = (DefaultTableModel) approvedCompoundTable.getModel();

        model.setRowCount(0);
        this.organization.getCompoundList().stream()
                .filter(com -> com.getStatus() == Status.APPROVED
                || com.getStatus() == Status.REJECTED)
                .forEach(c -> {
                    Object[] row = new Object[5];
                    row[0] = c;
                    row[1] = c.getFormula();
                    row[2] = c.getCreatedOn();
                    row[3] = c.getStatus().toString();
                    if (c.getStatus() == Status.REJECTED) {
                        WorkRequest request = retrieveWorkRequest(c);
                        row[4] = request.getComments();
                    } else {
                        row[4] = "";
                    }
                    model.addRow(row);
                });

    }

