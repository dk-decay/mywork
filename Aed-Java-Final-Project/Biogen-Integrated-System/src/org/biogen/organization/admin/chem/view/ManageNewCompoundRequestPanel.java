    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = newCompoundTable.getSelectedRow();
        if (selectedRow >= 0) {
            Compound compound = (Compound) newCompoundTable.getValueAt(selectedRow, 0);
            EditNewCompoundPanel panel = new EditNewCompoundPanel(system,
                    container, user, enterprise, organization, compound);
            CardLayout layout = (CardLayout) container.getLayout();
            container.add("EditNewCompoundPanel", panel);
            layout.next(container);
        } else {
            JOptionPane.showMessageDialog(null, "Please Select a compound request to be modified");
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = newCompoundTable.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete ?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Compound compound = (Compound) newCompoundTable.getValueAt(selectedRow, 0);
                enterprise.getCompoundDirectory().removeCompound(compound);
                organization.getCompoundList().remove(compound);
                this.populateNewCompoundRequest();
                updateWorkOrderRequest(compound);
                JOptionPane.showMessageDialog(null, "New Compound Request deleted succesfully");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please Select a compound request to be deleted");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    // cancel work order
    public void updateWorkOrderRequest(Compound compound) {
        WorkRequest workRequest = retrieveWorkRequest(compound);
        workRequest.setStatus(WorkRequestStatus.CANCELLED);

    }

    public WorkRequest retrieveWorkRequest(Compound compound) {

        WorkRequest request = this.enterprise.getWorkQueue().getWorkRequestList().stream()
                .filter(wr -> wr.getAttachment().getAttachmentType() == AttachmentType.NewCompoundApprovalAttachment)
                .filter(fwr -> {
                    NewCompoundApprovalAttachment attach
                            = (NewCompoundApprovalAttachment) fwr.getAttachment();
                    if (attach.getCompound() == compound && (fwr.getStatus()
                            == WorkRequestStatus.NEW || fwr.getStatus() == WorkRequestStatus.UPDATED)) {
                        return true;
                    } else {
                        return false;
                    }
                })
                .findFirst()
                .get();
        return request;
    }
    
