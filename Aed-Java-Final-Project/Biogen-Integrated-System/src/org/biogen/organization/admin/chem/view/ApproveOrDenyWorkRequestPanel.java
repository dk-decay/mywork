    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:

        this.workRequest.setEndDate(new Date());

        if (approvalStatusCombo.getSelectedItem().equals("APPROVED")) {
            this.workRequest.setStatus(WorkRequestStatus.APPROVED);
            this.workRequest.setComments("Request Approved");
            this.chemicalSynthesized.setQuantity(this.chemicalSynthesized.getQuantity() - this.attachment.getItemOrder().getQuantity());
            this.attachment.getItemOrder().setItemStatus(ItemStatus.APPROVED);

        } else {
            this.workRequest.setStatus(WorkRequestStatus.DENIED);
            this.workRequest.setComments("Request Rejected");
            this.attachment.getItemOrder().setItemStatus(ItemStatus.DENIED);
        }

        redirectUser();

    }//GEN-LAST:event_updateBtnActionPerformed

    public void redirectUser() {
        container.remove(this);
        Component[] componentArray = container.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageVendorRequest previousPanel = (ManageVendorRequest) component;
        previousPanel.populateVendorRequestTable();
        previousPanel.populateOtherRequestTable();
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);

    }
    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        redirectUser();
    }//GEN-LAST:event_backBtnActionPerformed

    private void synthesizeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_synthesizeBtnActionPerformed
        // TODO add your handling code here:
        this.organization.createOrUpdateOrder(attachment.getCompound(),
                Integer.valueOf(availableQuantityField.getText()));
        JOptionPane.showMessageDialog(null, "Inventory has been updated");
        populateForm();

    }//GEN-LAST:event_synthesizeBtnActionPerformed


