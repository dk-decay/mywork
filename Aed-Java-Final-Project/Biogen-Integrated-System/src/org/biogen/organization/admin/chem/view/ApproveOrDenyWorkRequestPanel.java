    public void populateForm() {

        requestedByField.setText(workRequest.getSender().toString());
        compoundField.setText(attachment.getCompound().toString());
        requestedQuantityField.setText(String.valueOf(attachment
                .getItemOrder().getQuantity()));

        availableQuantityField.setText(String.valueOf(this.chemicalSynthesized.getQuantity()));
        if (chemicalSynthesized.getQuantity() < attachment.getItemOrder().getQuantity()) {
            synthesizeBtn.setVisible(true);
            synthesizeBtn.setEnabled(true);
            updateBtn.setEnabled(false);
            availableQuantityField.setEnabled(true);
            availableQuantityField.setEditable(true);
        } else {
            synthesizeBtn.setVisible(false);
            synthesizeBtn.setEnabled(false);
            updateBtn.setEnabled(true);
            availableQuantityField.setEnabled(false);
            availableQuantityField.setEditable(false);

        }

    }

